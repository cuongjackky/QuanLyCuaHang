    private void LoadDI(){
        
        DefaultTableModel model = (DefaultTableModel)tableDI.getModel();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try{
            ResultSet rs = new DBUpdater().GetInfoDI(ImportID);
            if (!rs.next()){
                JOptionPane.showMessageDialog(null, "Không có thông tin phù hợp");
            }else{
                do{
                    String madn = rs.getString(1);
                    String masp = rs.getString(2);
                    String sl = rs.getString(3);
                    model.addRow(new Object[]{madn, masp, sl});
                }while(rs.next());
                
            }
            
        }catch(Exception e){
            e.printStackTrace();
        }
        diproductid.setText("");
        diproductname.setText("");
        disupplierid.setText("");
        disuppliername.setText("");
    }
