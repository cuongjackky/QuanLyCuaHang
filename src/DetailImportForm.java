            ResultSet rs = new DBUpdater().GetInfoProdForImportForm(masp);
            rs.next();
            diproductid.setText(masp);
            diproductname.setText(rs.getString(1));
            disupplierid.setText(rs.getString(2));
            disuppliername.setText(rs.getString(3));
