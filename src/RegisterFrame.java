    private void OkBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_OkBtnMouseClicked
        Boolean check = true;
        String MaKH = new Helper().generateUniqueCustomerId();
        String TaiKhoan =TaiKhoanTxt.getText();
        if(TaiKhoan.equals("")){
            TKWarn.setText("Nhập tài khoản");
            check =false;
        }
        else{
            if ((new DBUpdater().checkUniqueUserName(TaiKhoan))){
                TKWarn.setText("Tài khoản đã tồn tại");
                check = false;
            }
        }
        
       
        String MatKhau = MatKhauTxt.getText();
        if(MatKhau.equals("")){
            MKWarn.setText("Nhập mật khẩu");
            check =false;
        }
        String HoKH = HoTxt.getText();
        if(HoKH.equals("")){
            HoWarn.setText("Nhập họ");
            check =false;
        }
        String TenKH = TenTxt.getText();
        if(TenKH.equals("")){
            TenWarn.setText("Nhập tên");
            check =false;
        }
        String GioiTinh_KH = (String)GioiTinhCBox.getSelectedItem();
        if(GioiTinh_KH.equals("Nam")){
            GioiTinh_KH = "1";
        }
        else GioiTinh_KH = "0";
        
        String NgaySinh_KH = NgaySinhTxt.getText();
        if(NgaySinh_KH.equals("")){
            NgaySinhWarn.setText("Nhập ngày sinh");
            check =false;
        }
        String SDT_KH = SDTTxt.getText();
        if(SDT_KH.equals("")){
            SDTWarn.setText("Nhập số điện thoại");
            check =false;
        }
        String Email_KH = EmailTxt.getText();
        if(Email_KH.equals("")){
            EmailWarn.setText("Nhập email");
            check =false;
        }
         
        String Duong = DuongTxt.getText();
        if(Duong.equals("")){
            DuongWarn.setText("Nhập đường");
            check = false;
        }
        String SoNha = SoNhaTxt.getText();
        if(SoNha.equals("")){
            SoNhaWarn.setText("Nhập số nhà");
            check =false;
        }
        String Phuong = PhuongTxt.getText();
        if(Phuong.equals("")){
            PhuongWarn.setText("Nhập phường");
            check =false;
        }
        String Quan = QuanTxt.getText();
        if(Quan.equals("")){
            QuanWarn.setText("Nhập quận");
            check =false;
        }
        String ThanhPho = ThanhPhoTxt.getText();
        if(ThanhPho.equals("")){
            ThanhPhoWarn.setText("Nhập thành phố");
            check =false;
        }
        
        if(check == true){
            DBUpdater db = new DBUpdater();
            if(db.insertNewCustomerInformation(MaKH, HoKH, TenKH, GioiTinh_KH, NgaySinh_KH, SDT_KH, Email_KH, SoNha, Duong, Phuong, Quan, ThanhPho)&&db.insertNewCustomerAccount(MaKH, TaiKhoan, MatKhau)){
                JOptionPane.showMessageDialog(this, "Đăng ký thành công !!");
                this.setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(this, "Lỗi !!");
                
            }
        }
        
    }//GEN-LAST:event_OkBtnMouseClicked

