    private void importbtnOKMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_importbtnOKMouseClicked
        String madn=importID.getText();
        String makho = importwarehouseid.getText();
        String date=importdate.getText();
        Boolean check =  true;

        if (tableImportProduct.getRowCount()==0){
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm nhập hàng!");

        }else{
            int r = new DBUpdater().InsertNewImportForm(madn, date, makho, StaffID);
            int r1=-1;
            for(int i=0; i<tableImportProduct.getRowCount();i++){

                String masp = tableImportProduct.getValueAt(i, 0).toString();
                String sl = tableImportProduct.getValueAt(i, 4).toString();
                r1 = new DBUpdater().InsertNewDetailImport(madn, masp, Integer.parseInt(sl),StoreID);
                if (r1<0){
                    JOptionPane.showMessageDialog(this, "Thêm đơn nhập thất bại");
                    break;
                }
            }
            if (r1>0 && r>0){
                JOptionPane.showMessageDialog(this, "Thêm đơn nhập thành công!");

            }

        }

    }//GEN-LAST:event_importbtnOKMouseClicked

