    private void LoadTableOrder(){
        DefaultTableModel model = (DefaultTableModel)tableOrder.getModel();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try{
            ResultSet rs=  new DBUpdater().GetOrderListByStaffID(staffID);
            if (!rs.next()){
                JOptionPane.showMessageDialog(this, "Không tìm thấy đơn hàng phù hợp!");
            }else{
                do{
                    String madh =rs.getString(1);
                    String makh = rs.getString(2);
                    String ngaydat =rs.getString(4);
                    String tongtiensp = rs.getString(5);
                    String phivc = rs.getString(6);
                    String tongtien = rs.getString(7);
                    String sonha= rs.getString(8);
                    String duong= rs.getString(9);
                    String phuong= rs.getString(10);
                    String quan= rs.getString(11);
                    String tp= rs.getString(12);
                    String diachi = sonha + ", " + duong + ", " + phuong + 
                            ", " + quan + ", " + tp;
                    String thanhtoan = rs.getString(13);
                    String trangthai = rs.getString(14);
                    model.addRow(new Object[]{madh,makh,ngaydat,tongtiensp,phivc,tongtien,diachi,thanhtoan,trangthai});
                    
                }while(rs.next());
            }
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }
