    private void infobtnAcceptMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_infobtnAcceptMouseClicked
        String pass = String.valueOf(infoNewPass.getPassword());
        String retype = String.valueOf(infoRetypePass.getPassword());
        Boolean check = true;
        if (pass.length()>20){
            JOptionPane.showMessageDialog(this, "Mật khẩu tối đa 20 ký tự!");
            check=false;
        }
        if (!pass.equals(retype)){
            JOptionPane.showMessageDialog(this, "Mật khẩu không trùng khớp!");
            check=false;
        }
        if (check){
            //update
            int result =new DBUpdater().UpdatePassStaff(staffID, pass);
            if (result>0){
                JOptionPane.showMessageDialog(this, "Cập nhật mật khẩu thành công!");
                Refesh();
            }else{
                JOptionPane.showMessageDialog(this, "Cập nhật mật khẩu thất bại!");
                Refesh();
            }
        }
    }//GEN-LAST:event_infobtnAcceptMouseClicked

