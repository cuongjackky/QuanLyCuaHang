    private void ConfirmBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ConfirmBtnMouseClicked
        Boolean check = true;
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            if(model.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "Giỏ hàng trống !!");
                check = false;
            }
        String Duong = DuongTxt.getText();
        
        if(Duong.equals("")){
            DuongWarn.setText("Nhập đường");
            check = false;
        }
        String SoNha = SoNhaTxt.getText();
        if(SoNha.equals("")){
            SoNhaWarn.setText("Nhập số nhà");
            check =false;
        }
        String Phuong = PhuongTxt.getText();
        if(Phuong.equals("")){
            PhuongWarn.setText("Nhập phường");
            check =false;
        }
        String Quan = QuanTxt.getText();
        if(Quan.equals("")){
            QuanWarn.setText("Nhập quận");
            check =false;
        }
        String ThanhPho = ThanhPhoTxt.getText();
        if(ThanhPho.equals("")){
            ThanhPhoWarn.setText("Nhập thành phố");
            check =false;
        }
        
        if(check == true){
            String MaDH = new Helper().generateUniqueOrderId();
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");  
            LocalDateTime now = LocalDateTime.now();
            String NgayDat = dtf.format(now);
            String HinhThucThanhToan = (String)ThanhToanCBox.getSelectedItem();
            String TrangThai = "Chờ giao";
            DBUpdater db = new DBUpdater();
            String MaNV = db.getSaleManId(StoreId);
            Boolean flag = true;
            if(db.insertNewOrder(MaDH, customerId, StoreId, NgayDat, SoNha, Duong, Phuong, Quan, ThanhPho, HinhThucThanhToan, TrangThai, MaNV)){
                
            }
            else flag = false;
            for(int i =0;i<model.getRowCount();i++){
                String MaSP = (String)model.getValueAt(i, 0);
                String SoLuongSanPham =(String)model.getValueAt(i, 2);
                if(!db.insertNewOrderDetail(MaDH, MaSP, SoLuongSanPham)){
                    flag = false;
                }
            }
            if(flag==true){
                jTable1.setModel(db.getOrderHistory(customerId));
                
                while(model.getRowCount()>0){
                    model.removeRow(0);
                }
                TotalCost=0;
                StoreId ="";
                TongTienTxt.setText("");
                PhiVanChuyenTxt.setText("");
                JOptionPane.showMessageDialog(this, "Thành công !!");
            }
            else{
                JOptionPane.showMessageDialog(this, "Lỗi !!");
            }
            
        }
    }//GEN-LAST:event_ConfirmBtnMouseClicked

